# 5.How Tuple is beneficial as compare to the list?

# Tuple are enclosed in '()' whereas List is enclosed in '[]'
# Tuple includes data which are immutable whereas list includes mutable data set
# Tuples cannot be modified in any manner as oppose to list
# Tuples are more efficient and faster than list
# We cannot create a Tuple with only one element in it but creating a list with one element is
